export function flattenPersons() {
  const list = [];
  for (const [group, items] of Object.entries(DIMENSIONS.person.groups)) {
    for (const name of items) {
      list.push({ name, group });
    }
  }
  return list;
}

export function getAllPersonNames() {
  return flattenPersons().map((p) => p.name);
}

